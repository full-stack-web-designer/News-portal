		The fashion industry churns out an estimated 100 billion garments annually. For the moment, we are not as trend driven as other markets, though we do possess a few of our own. Ahead of the Spring Summer 2020 fashion week, we take a closer look at a few which require change for the better.<br>

		Barring bridal, almost every designer in India works with handloom. Many also have the privilege to work with it at the yarn, dyeing and weaving stages. No doubt, our nation’s fabric is truly special: rich with history and significance. The current reality is more complicated, shrouded in grey. Over time, the definition of ‘hand spun and hand woven’ has changed, with most readily-available khadi — including the fabric sold at the government-run Khadi Villages and Industry Commission (KVIC) — being woven on semi-mechanised charkas, and not the manual Ambar charka. This is identifiable by the flat, even weave.
